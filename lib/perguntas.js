export function calcularPercentuais(respostas, conjuge) {
  const resultados = {}
  const todos = [...BLOCOS.bloco1, ...BLOCOS.bloco2]
  todos.forEach(comportamento => {
    let soma = 0
    for (let i = 1; i <= 7; i++) {
      const chave = `${comportamento}_${i}`
      soma += parseInt(respostas[chave] || 0)
    }
    resultados[comportamento] = parseFloat(((soma / 35) * 100).toFixed(1))
  })
  return resultados
}

export function classificar(pct) {
  if (pct <= 33) return 'baixo'
  if (pct <= 65) return 'medio'
  return 'elevado'
}
